    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        Transaction transaction = new Transaction();
        transaction.setIdtransaction(Integer.parseInt(txtIdTransaction.getText()));
        transaction.setPaydate(txtPayDate.getText());
        transaction.setQtyticket(Integer.parseInt(txtQtyTicket.getText()));
        transaction.setTicketprice(Integer.parseInt(txtTicketPrice.getText()));
        transaction.setTotalpay(Integer.parseInt(txtTotalPay.getText()));
        transaction.setReservation((Reservation)cmbReservation.getSelectedItem());
        transaction.save();
        
        txtIdTransaction.setText(Integer.toString(transaction.getIdtransaction()));
        
        tampilkanData();
    }//GEN-LAST:event_btnSaveActionPerformed

