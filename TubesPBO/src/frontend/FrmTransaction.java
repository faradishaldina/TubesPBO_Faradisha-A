    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)tblTransaction.getModel();
        int row = tblTransaction.getSelectedRow();
        
        Transaction transaction = new Transaction().getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        transaction.delete();
        kosongkanForm();
        tampilkanData();
    }//GEN-LAST:event_btnRemoveActionPerformed

    private void tblTransactionMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTransactionMouseClicked
        // TODO add your handling code here:
         DefaultTableModel model = (DefaultTableModel)tblTransaction.getModel();
        int row = tblTransaction.getSelectedRow();
        Transaction transaction = new Transaction();
        
        transaction = transaction.getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        
        txtIdTransaction.setText(String.valueOf(transaction.getIdtransaction()));
        txtPayDate.setText(transaction.getPaydate());
        txtQtyTicket.setText(String.valueOf(transaction.getQtyticket()));
        txtTicketPrice.setText(String.valueOf(transaction.getTicketprice()));
        txtTotalPay.setText(String.valueOf(transaction.getTotalpay()));
        cmbReservation.getModel().setSelectedItem(transaction.getReservation().getBookingdate());
    }//GEN-LAST:event_tblTransactionMouseClicked

