    public void tampilkanData() {
        String[] kolom = {"ID Train", "Train Name", "Departure Time", "Ticket Price", "Origin", "Destination"};
        ArrayList<Train> list = new Train().getAll();
        Object rowData[] = new Object[7];
        
        tblTrain.setModel(new DefaultTableModel(new Object[][] {}, kolom));
        
        for(Train k : list) {
            rowData[0] = k.getIdtrain();
            rowData[1] = k.getTrainname();
            rowData[2] = k.getDeparturetime();
            rowData[3] = k.getTicketprice();
            rowData[4] = k.getOrigin();
            rowData[5] = k.getDestination();
            
            ((DefaultTableModel)tblTrain.getModel()).addRow(rowData);
        }
    }
    
    public void search(String keyword) {
        String[] kolom = {"ID Train", "Train Name", "Departure Time", "Ticket Price", "Origin", "Destination"};
        ArrayList<Train> list = new Train().search(keyword);
        Object rowData[] = new Object[7];
        
        tblTrain.setModel(new DefaultTableModel(new Object[][] {}, kolom));
        
        for(Train k : list) {
            rowData[0] = k.getIdtrain();
            rowData[1] = k.getTrainname();
            rowData[2] = k.getDeparturetime();
            rowData[3] = k.getTicketprice();
            rowData[4] = k.getOrigin();
            rowData[5] = k.getDestination();
            
            ((DefaultTableModel)tblTrain.getModel()).addRow(rowData);
        }
    }
