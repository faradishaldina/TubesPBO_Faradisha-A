    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        Train train = new Train();
        train.setIdtrain(Integer.parseInt(txtIdTrain.getText()));
        train.setTrainname(txtTrainName.getText());
        train.setDeparturetime(txtDepartureTime.getText());
        train.setTicketprice(Integer.parseInt(txtTicketPrice.getText()));
        train.setOrigin(txtOrigin.getText());
        train.setDestination(txtDestination.getText());
        train.save();
        
        txtIdTrain.setText(Integer.toString(train.getIdtrain()));
        
        tampilkanData();
    }//GEN-LAST:event_btnSaveActionPerformed

