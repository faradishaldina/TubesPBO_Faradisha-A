    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)tblTrain.getModel();
        int row = tblTrain.getSelectedRow();
        
        Train train = new Train().getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        train.delete();
        kosongkanForm();
        tampilkanData();
    }//GEN-LAST:event_btnRemoveActionPerformed

    private void tblTrainMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTrainMouseClicked
        // TODO add your handling code here:
         DefaultTableModel model = (DefaultTableModel)tblTrain.getModel();
        int row = tblTrain.getSelectedRow();
        Train train = new Train();
        
        train = train.getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        
        txtIdTrain.setText(String.valueOf(train.getIdtrain()));
        txtTrainName.setText(train.getTrainname());
        txtDepartureTime.setText(train.getDeparturetime());
        txtTicketPrice.setText(String.valueOf((train.getTicketprice())));
        txtOrigin.setText(train.getOrigin());
        txtDestination.setText(train.getDestination());
    }//GEN-LAST:event_tblTrainMouseClicked

